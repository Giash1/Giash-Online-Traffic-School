export function t(language: LanguageCode | string | undefined, key: TranslationKey, vars?: Record<string, string | number>): string {
  const lang = (language as LanguageCode) ?? 'eng';
  const template = translations[lang]?.[key] ?? translations.eng[key];
  if (!vars) return template;
  return Object.keys(vars).reduce(
    (acc, k) => acc.replace(new RegExp(`\\{${k}\\}`, 'g'), String(vars[k])),
    template
  );
}
